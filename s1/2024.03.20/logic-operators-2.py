print('Operador AND:')
print(f'V e V -> {True and True}')
print(f'V e F -> {True and False}')
print(f'F e F -> {False and False}')

print('\nOperador OR:')
print(f'V ou V -> {True or True}')
print(f'V ou F -> {True or False}')
print(f'F ou F -> {False or False}')

print('\nOperador NOT:')
print(f'not V -> {not True}')
print(f'not F -> {not False}')